package com.zltel.broadcast.common.systemcapacity.bean;

public class Capability {
    private Long total;
    private Long used;

    /**
     * @return the total
     */
    public Long getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(Long total) {
        this.total = total;
    }

    /**
     * @return the used
     */
    public Long getUsed() {
        return used;
    }

    /**
     * @param used the used to set
     */
    public void setUsed(Long used) {
        this.used = used;
    }


}
